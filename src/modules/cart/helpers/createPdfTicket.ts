//в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ DescripciГіn рџ“ќ в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ//
// Se encarga de generar un archivo PDF con toda la informaciГіn del ticket de compra,
// incluyendo encabezado, tabla de productos y totales. EstГЎ pensado para ser utilizado
// en el flujo de ventas del sistema y permitir la descarga inmediata del comprobante.

//в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ Funciones рџ”§ в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ//
// - createPdfTicket utiliza la librerГ­a jsPDF para crear y manipular el documento PDF.
// - Emplea jspdf-autotable (via applyPlugin) para generar automГЎticamente la tabla de productos.
// - Usa la funciГіn formatCurrency para formatear valores monetarios segГєn la configuraciГіn local.
// - Implementa una funciГіn interna "truncate" para evitar desbordes de texto en la tabla.
// - Calcula dinГЎmicamente la posiciГіn final de la tabla para ubicar correctamente los totales.
// - Genera el archivo final y lo descarga automГЎticamente con un nombre basado en el ID del ticket.

//в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ Notas tГ©cnicas рџ’Ѕ в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ//
// - Se usa `applyPlugin(jsPDF)` en vez del import default de autoTable porque en este
//   proyecto (Vite) el default export de "jspdf-autotable" v3 se resuelve como el mГіdulo
//   completo en lugar de la funciГіn, rompiendo `autoTable(doc, options)`.
//   `applyPlugin` registra `autoTable` como mГ©todo en el prototipo de jsPDF de forma segura.
// - El augment de tipos de abajo declara `doc.autoTable` y `doc.lastAutoTable`, que no
//   vienen tipados en el paquete base de jsPDF.

//-----------------------------------------------------------------------------//

import jsPDF from "jspdf";
import { applyPlugin } from "jspdf-autotable";
import { formatCurrency } from "./formatCurrency";
import type { SellTicketType } from "../../../typings/sells/types/sellsTypes";

declare module "jspdf" {
  interface jsPDF {
    autoTable: (options: Record<string, unknown>) => jsPDF;
    lastAutoTable: { finalY: number };
  }
}

applyPlugin(jsPDF);

export const createPdfTicket = (ticket: SellTicketType): void => {
  const doc = new jsPDF();

  //в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ Encabezado в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ//
  doc.setFontSize(16);
  doc.text("Comprobante de Venta", 14, 20);
  doc.setFontSize(10);
  doc.text(`Ticket NВє: ${ticket._id}`, 14, 28);
  doc.text(`Fecha: ${new Date().toLocaleString("es-AR")}hs`, 14, 34);
  doc.text(`Cajero: ${ticket.seller_name}`, 14, 40);
  doc.text(`MГ©todo de pago: ${ticket.payment_method}`, 14, 46);

  //в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ Tabla de productosв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ//
  const truncate = (val: string) =>
    val?.length > 25 ? val.slice(0, 22) + "..." : val;

  const productRows = ticket.products.map((p) => [
      truncate(p.name),
      truncate(p.sku),
      truncate(p.model_type + ' ' + p.model_size),
      truncate(String(p.stock_required)),
      truncate(formatCurrency(p.price)),
      truncate(formatCurrency(p.price * p.stock_required)),
  ]);

  doc.autoTable({
    head: [["Producto", "SKU", "Tipo", "Cant.", "P. Unitario", "P. Total"]],
    body: productRows,
    startY: 55,
  });

  //в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ Totales в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ//

  const finalY = doc.lastAutoTable.finalY + 10;
    //в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ Subtotal mГЎs pequeГ±o в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ//

    doc.setFontSize(10);
    doc.text(`Subtotal: ${formatCurrency(ticket.sub_total)}`, 14, finalY);
    //в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ IVA mГЎs pequeГ±o в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ//

    doc.setFontSize(10);
    doc.text(`IVA (${ticket.iva}%): ${formatCurrency(ticket.sub_total * ticket.iva / 100)}`, 14, finalY + 6);
    //в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ Total mГЎs grande y destacado в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ//

    doc.setFontSize(14);
    doc.text(`Total: ${formatCurrency(ticket.total_amount)}`, 14, finalY + 12);

  //в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ Descargar PDF в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ//
  doc.save(`ticket_${ticket._id}.pdf`);
};